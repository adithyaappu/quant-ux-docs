<template><div><h1 id="create-user-tests-for-your-app" tabindex="-1"><a class="header-anchor" href="#create-user-tests-for-your-app"><span>Create User Tests for Your App</span></a></h1>
<p>After designing the app, it is time for testing. Usually there will be a set of tasks or steps that need to be evaluated using the prototype. In Quant-UX these tasks can be specified under a test. A test is considered successful when the user manages to perform all the listed tasks.</p>
<ol>
<li>Go back to the prototypes page of prototypes and select the <code v-pre>Test</code>.</li>
</ol>
<p><img src="@source/content/getting-started/media/create-user-tests/1-prototype-page.png" alt="prototype page with test highlighted"></p>
<ol start="2">
<li>Write a welcome message for the user in the box and check the <code v-pre>Record data only for the first time a user tests the prototype</code>.</li>
</ol>
<p><img src="@source/content/getting-started/media/create-user-tests/2-welcome-message.png" alt="welcome message"></p>
<ol start="3">
<li>Click on the <code v-pre>Add Task</code> button</li>
<li>In the new window add a <code v-pre>Task Name</code>, uncheck the <code v-pre>Record only screen views</code>.</li>
<li>Next specify and record the task, which the user should be completed. To do this simply perform the task on the prototype shown on the right side of this window (3). Each action will be recorded in the <code v-pre>Expected Steps</code> as a flow.</li>
</ol>
<p><img src="@source/content/getting-started/media/create-user-tests/3-add-task.png" alt="Task specification page"></p>
<ol start="6">
<li>Click on the <code v-pre>Click Me</code> button in the prototype, which will open up the second screen. Then, click on the <code v-pre>Back</code> button to come back to the first screen. Ensure that the actions are correctly captured in the <code v-pre>Expected Steps</code> box.</li>
</ol>
<div class="hint-container tip">
<p class="hint-container-title">Tips</p>
<p>In case of any mistake you can delete specific step and do again. To delete hover over any step in the flow and it will show and option to <code v-pre>Remove</code>.</p>
</div>
<ol start="7">
<li>Add a <code v-pre>Description</code> for the task so that the user will get a clear idea about the tasks to be performed.</li>
</ol>
<p><img src="@source/content/getting-started/media/create-user-tests/4-task-captured.png" alt="Showing captured task"></p>
<ol start="8">
<li>Save the task to close the window.</li>
</ol>
